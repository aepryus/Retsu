import UIKit

extension UIFont {
    static func optima(size: CGFloat) -> UIFont { UIFont(name: "Optima", size: size)! }
    static func avenir(size: CGFloat) -> UIFont { UIFont(name: "AvenirNext-Regular", size: size)! }
}
